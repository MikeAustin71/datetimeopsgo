// IanaTimeZones - This type and its associated methods encapsulate all 590+
// IANA Time Zones. This type is therefore used as an enumeration of the IANA
// Time Zones.
//
// The Go Programming Language uses IANA Time Zones in date-time calculations.
//	Reference:
//		https://golang.org/pkg/time/#LoadLocation
//
// IANA Time Zones are widely recognized as the the world's leading authority on
// time zones.
// 	Reference:
//		https://en.wikipedia.org/wiki/List_of_tz_database_time_zones
//		https://en.wikipedia.org/wiki/Tz_database
//
// The IANA Time Zone data base and reference information is located at:
//	https://www.iana.org/time-zones.
//
// For easy access to the IANA Time Zones it is recommended that you use the
// global variable 'IanaTz' declared below. This variable instantiates the
// 'IanaTimeZones' type. It is therefore much easier to access any of the 590+
// IANA time zones using dot operators and intelliSense (a.k.a. intelligent code
// completion).
//	Examples:
//		IanaTz.US.Central()    - US Central Time Zone
//		IanaTz.US.Eastern()    - US Eastern Time Zone
//		IanaTz.US.Mountain()   - US Mountain Time Zone
//		IanaTz.US.Pacific()    - US Pacific Time Zone
//		IanaTz.Europe.London() - Europe/London Time Zone
//		IanaTz.Asia.Shanghai() - Asia/Shanghai Time Zone
//
